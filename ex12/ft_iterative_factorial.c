int	ft_iterative_factorial(int nb)
{
	int n;

	n = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	if (nb > 12)
		return (0);
	if (nb > 0)
	{
		while (nb >= 1)
		{
			n *= nb;
			nb--;
		}
	}
	return (n);
}
