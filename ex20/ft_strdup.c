#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		x;
	char	*dest;

	x = 0;
	while (src[x] != '\0')
		x++;
	dest = malloc(sizeof(dest) * x + 1);
	x = 0;
	while (src[x] != '\0')
	{
		dest[x] = src[x];
		x++;
	}
	dest[x] = '\0';
	return (dest);
}
